#include "testfunktioner.h"

float unity_test_omkrets(float nr1, float nr2, float nr3, float nr4)
    {
    return ((nr1) + (nr2) + (nr3) + (nr4));
    }

float unity_test_area(float nr1, float nr2)
        {
        return ((nr1) * (nr2));
        }

float unity_test_volym(float nr1, float nr2, float nr3)
    {
    return ((nr1) * (nr2) * (nr3));
    }
